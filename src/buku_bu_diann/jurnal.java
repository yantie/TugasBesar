    private void jTextField5KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField5KeyPressed
        // TODO add your handling code here:
        DefaultTableModel tabelnyo = new DefaultTableModel();
        tabelnyo.addColumn("No");
        tabelnyo.addColumn("Nama ");
        tabelnyo.addColumn("Judul");
        try{
            koneksi();
            String sql = "Select * from jurnal where no like '%" + jTextField2.getText() + "%'" +
                    "or nama like '%" + jTextField3.getText() + "%'";
            ResultSet rs = cn.executeQuery(sql);
            while (rs.next()) {
                tabelnyo.addRow(new Object[]{
                    rs.getString(1),
                    rs.getString(2),
                    rs.getString(3)
                });
            }
            jTable1.setModel(tabelnyo);
        }catch (Exception e){
        }
    }//GEN-LAST:event_jTextField5KeyPressed

    
