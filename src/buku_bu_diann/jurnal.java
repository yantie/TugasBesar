    public void jur(){
       try{
           koneksi();
           String sql = "Select * from jurnal where no='"+jTextField2.getText()+"'";
           ResultSet rs = cn.executeQuery(sql);
           if (rs.next())
           {
               jTextField2.setText(rs.getString(1));
               jTextField3.setText(rs.getString(2));
               jTextField4.setText(rs.getString(3));
               JOptionPane.showMessageDialog(null,"No : " + jTextField2.getText() + " sudah ada.!!!");
               kosongkan();
           }
       } catch (Exception e){}
       jTextField2.requestFocus();
}
    
